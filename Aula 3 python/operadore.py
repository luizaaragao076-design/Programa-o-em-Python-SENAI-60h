# operadores aritméticos
# +  -   *    /   //   %   **  = a  saída é um número

# básicos
print(10+2)
print(10-2)
print(10*2)
print(10/2)
# potencia
print(10**2)
# div c/ 2 barras
print(10//2)
# módulo -  resto da conta de divisão
print(10%2)

# raiz

raiz  =  16**(0.5)
print('raiz quadrada', raiz)



# operadores lógicos  -  saída é um dado lógico
# True ou False

print(10 > 2)
print(10 < 2)
print(10 >= 2)
print(10 <= 2)
print(10 == 2)
print(10 != 2)


 








